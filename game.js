var ysdk = null;       // объект SDK
var яПлеер = null;     // объект Player
var sdkГотов = false;

// Инициализация SDK
function initSDK() {
    if (typeof YaGames === 'undefined') {
        // Локальное тестирование — SDK недоступен
        console.log('YaGames SDK не найден — локальный режим');
        sdkГотов = false;
        return;
    }
    YaGames.init().then(function(sdk) {
        ysdk = sdk;
        sdkГотов = true;
        console.log('Яндекс SDK инициализирован');

        // Получаем объект игрока
        ysdk.getPlayer().then(function(player) {
            яПлеер = player;
            console.log('Игрок получен, авторизован:', player.getMode() !== 'lite');
        }).catch(function(e) {
            console.warn('Ошибка получения игрока:', e);
        });

        // Загружаем облачные сохранения (callback-версия)
        загрузитьОблако(null);

        // Сообщаем SDK что игра загружена
        ysdk.features.LoadingAPI && ysdk.features.LoadingAPI.ready();

    }).catch(function(e) {
        console.warn('Ошибка инициализации SDK:', e);
        sdkГотов = false;
    });
}

// ─── ОБЛАЧНЫЕ СОХРАНЕНИЯ ─────────────────────────────────────
function сохранитьОблако() {
    if (!яПлеер) { сохранитьИгру(); return; } // fallback на localStorage
    var данные = {
        игра: игра,
        дневник: дневник,
        историяВыборов: историяВыборов,
        флагиВыборов: флагиВыборов,
        посещённые: Array.from(картаБункера.посещённые),
        версия: 5
    };
    яПлеер.setData(данные, true).then(function() {
        console.log('Облачное сохранение OK');
    }).catch(function(e) {
        console.warn('Ошибка облачного сохранения, fallback:', e);
        сохранитьИгру();
    });
}

// колбэк вызывается когда данные точно загружены (или провалились)
function загрузитьОблако(колбэк) {
    if (!яПлеер) {
        загрузитьИгру();
        if (колбэк) колбэк();
        return;
    }
    яПлеер.getData().then(function(данные) {
        if (данные && данные.версия === 5 && данные.игра) {
            Object.assign(игра, данные.игра);
            дневник = данные.дневник || [];
            историяВыборов = данные.историяВыборов || [];
            Object.assign(флагиВыборов, данные.флагиВыборов || {});
            if (данные.посещённые) {
                данные.посещённые.forEach(function(к) { картаБункера.посещённые.add(к); });
            }
            console.log('Облачный прогресс загружен, день:', игра.день);
        }
        if (колбэк) колбэк();
    }).catch(function(e) {
        console.warn('Ошибка загрузки облака:', e);
        загрузитьИгру();
        if (колбэк) колбэк();
    });
}

// ─── РЕКЛАМА ─────────────────────────────────────────────────
var рекламаПоказывается = false;
var последняяРеклама = 0;
var ИНТЕРВАЛ_РЕКЛАМЫ = 180000; // 3 минуты между показами

function показатьРекламу(колбэк) {
    var сейчас = Date.now();
    if (!sdkГотов || рекламаПоказывается) {
        if (колбэк) колбэк();
        return;
    }
    if (сейчас - последняяРеклама < ИНТЕРВАЛ_РЕКЛАМЫ) {
        if (колбэк) колбэк();
        return;
    }

    рекламаПоказывается = true;
    // Ставим игру на паузу
    if (аудио && аудио.пауза) аудио.пауза();

    ysdk.adv.showFullscreenAdv({
        callbacks: {
            onOpen: function() {
                рекламаПоказывается = true;
            },
            onClose: function(wasShown) {
                рекламаПоказывается = false;
                последняяРеклама = Date.now();
                if (аудио && аудио.возобновить) аудио.возобновить();
                if (колбэк) колбэк();
            },
            onError: function(e) {
                рекламаПоказывается = false;
                console.warn('Ошибка рекламы:', e);
                if (колбэк) колбэк();
            }
        }
    });
}

// Показ рекламы каждые 5 дней
function проверитьРекламуПоДню(день) {
    if (день > 1 && день % 5 === 0) {
        показатьРекламу(null);
    }
}

