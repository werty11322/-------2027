function обновитьИнтерфейс() {
    var карта = {
        'здоровье-знач':  Math.max(0, игра.здоровье),
        'рассудок-знач':  Math.max(0, игра.рассудок),
        'еда-знач':       Math.max(0, игра.еда),
        'вода-знач':      Math.max(0, игра.вода),
        'энергия-знач':   Math.max(0, игра.энергия),
        'день-знач':      игра.день,
        'репутация-знач': игра.репутация
    };
    Object.keys(карта).forEach(function(ид) {
        var эл = document.getElementById(ид);
        if (эл) эл.textContent = карта[ид];
    });
    var критичные = { 'блок-здоровье': игра.здоровье, 'блок-рассудок': игра.рассудок, 'блок-еда': игра.еда, 'блок-вода': игра.вода };
    Object.keys(критичные).forEach(function(ид) {
        var блок = document.getElementById(ид);
        if (!блок) return;
        if (критичные[ид] <= 20) { блок.style.color = '#ff4444'; }
        else { блок.style.color = ''; }
    });
    обновитьВремя();
    обновитьДеградацию();
    var бар = document.getElementById('прогресс-дней');
    if (бар) бар.style.width = (игра.день / 30 * 100) + '%';
    обновитьИнвентарь();
    обновитьНПСПанель();
    проверитьСкрытыеВетки();
}

