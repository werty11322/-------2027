        кнопкаРестарт.style.cssText = 'display:block;margin:30px auto;padding:20px 40px;font-size:20px;background:linear-gradient(135deg,#e94560 0%,#dc2626 100%);color:white;border:none;border-radius:12px;cursor:pointer;font-weight:bold';
