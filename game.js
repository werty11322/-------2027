function показатьСпрайт(персонаж) {
    var слой = document.getElementById('слой-спрайтов');
    var спрайт = document.getElementById('спрайт-персонажа');
    if (!слой || !спрайт) return;

    if (!персонаж || !спрайты[персонаж]) {
        спрайт.style.opacity = '0';
        спрайт.style.transform = 'translateY(40px) scale(0.92)';
        setTimeout(function() {
            спрайт.style.display = 'none';
            спрайт.src = '';
        }, 500);
        return;
    }

    // Сначала скрываем старый спрайт
    спрайт.style.opacity = '0';
    спрайт.style.transform = 'translateY(40px) scale(0.92)';

    setTimeout(function() {
        var путь = спрайты[персонаж];
        спрайт.style.display = 'block';
        спрайт.style.transition = 'none';

        // Сбрасываем размеры — каждый спрайт подгоняем под контейнер
        спрайт.style.width = 'auto';
        спрайт.style.height = 'auto';
        спрайт.style.maxHeight = '420px';
        спрайт.style.maxWidth = '280px';
        спрайт.style.objectFit = 'contain';
        спрайт.style.objectPosition = 'bottom center';

        function показать() {
            setTimeout(function() {
                спрайт.style.transition = 'opacity 0.6s ease, transform 0.6s cubic-bezier(0.4,0,0.2,1)';
                спрайт.style.opacity = '0.88';
                спрайт.style.transform = 'translateY(0) scale(1)';
            }, 30);
        }

        if (спрайт.src.endsWith(путь.split('/').pop()) && спрайт.complete && спрайт.naturalHeight > 0) {
            // Та же картинка уже загружена
            показать();
        } else {
            спрайт.onload = показать;
            спрайт.onerror = function() { спрайт.style.display = 'none'; };
            спрайт.src = путь;
        }
    }, 300);
}

function скрытьСпрайт() {
    показатьСпрайт(null);
}


