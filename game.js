// ─── АВТОМАТИЧЕСКИЙ РАСХОД РЕСУРСОВ ─────────────────────────────────────────
function применитьСуточныйРасход() {
    var нпсМножитель = 1 + игра.нпс.length * 0.3; // NPC потребляют ресурсы

    // Базовый расход еды и воды каждый день
    игра.еда = Math.max(0, игра.еда - Math.round(4 * нпсМножитель));
    игра.вода = Math.max(0, игра.вода - Math.round(3 * нпсМножитель)); // снижен с 5 до 3
    игра.энергия = Math.max(0, игра.энергия - 3);

    // Ночь — холод, здоровье чуть падает
    if (игра.времяСуток === 'ночь') игра.здоровье = Math.max(0, игра.здоровье - 2);

    // Очень мало еды/воды — здоровье падает
    if (игра.еда < 15) игра.здоровье = Math.max(0, игра.здоровье - 5);
    if (игра.вода < 15) игра.здоровье = Math.max(0, игра.здоровье - 8);

    // Очень мало здоровья — рассудок падает
    if (игра.здоровье < 20) игра.рассудок = Math.max(0, игра.рассудок - 3);

    // Предупреждения
    if (игра.еда <= 20) { показатьУведомление('⚠️ Еда заканчивается!'); аудио.тревога(); }
    if (игра.вода <= 20) { показатьУведомление('⚠️ Вода заканчивается!'); аудио.тревога(); }
    if (игра.здоровье <= 20) { аудио.запуститьСердце(); }
    else { аудио.остановитьСердце(); }
    if (игра.рассудок <= 25 && Math.random() < 0.4) { setTimeout(аудио.шёпот, 500 + Math.random() * 2000); }
}


