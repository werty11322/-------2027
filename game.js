// ─── ПОСИМВОЛЬНЫЙ ВЫВОД (печатная машинка) ───────────────────────────────────
var печатаетСейчас = false;
var текущийТаймерПечати = null;
var текущийКолбэк = null;

function пропуститьПечать() {
    if (!печатаетСейчас) return;
    if (текущийТаймерПечати) { clearTimeout(текущийТаймерПечати); текущийТаймерПечати = null; }
    var поле = document.getElementById('текст-диалога');
    if (поле && поле.dataset.полныйТекст) поле.textContent = поле.dataset.полныйТекст;
    печатаетСейчас = false;
    if (текущийКолбэк) { var cb = текущийКолбэк; текущийКолбэк = null; cb(); }
}

function печататьТекст(элемент, текст, скорость, колбэк) {
    if (!элемент) { if (колбэк) колбэк(); return; }
    if (текущийТаймерПечати) { clearTimeout(текущийТаймерПечати); текущийТаймерПечати = null; }

    печатаетСейчас = true;
    текущийКолбэк = колбэк || null;
    элемент.textContent = '';
    элемент.classList.remove('печатная-машинка');
    элемент.dataset.полныйТекст = текст;

    // Скорость: базовая 18мс, быстрее чем раньше
    var базСкорость = скорость || 18;
    var индекс = 0;

    function следующийСимвол() {
        if (индекс >= текст.length) {
            печатаетСейчас = false;
            текущийКолбэк = null;
            if (колбэк) колбэк();
            return;
        }
        // Добавляем сразу по 2-3 символа для скорости
        var порция = Math.min(индекс + 2, текст.length);
        элемент.textContent = текст.substring(0, порция);
        индекс = порция;

        var символ = текст[индекс - 1];
        var пауза = базСкорость;
        if (символ === '.' || символ === '!' || символ === '?') пауза = базСкорость * 6;
        else if (символ === ',' || символ === ';') пауза = базСкорость * 3;
        if (игра.рассудок < 30) пауза += Math.random() * 40 - 10;

        текущийТаймерПечати = setTimeout(следующийСимвол, Math.max(5, пауза));
    }
    следующийСимвол();
}


