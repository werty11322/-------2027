// ─── СДЕЛАТЬ ВЫБОР ────────────────────────────────────────────────────────────
function сделатьВыбор(индекс, активныеВыборы) {
    // Разблокируем аудио только при первом клике — браузер требует user gesture
    if (!window._аудиоРазблокирован) {
        window._аудиоРазблокирован = true;
        аудио.запуститьФон();
    }
    var текущиеВыборы = активныеВыборы || (сценарии[игра.текущийДиалог] && сценарии[игра.текущийДиалог].выборы);
    if (!текущиеВыборы || !текущиеВыборы[индекс]) return;

    var всеКнопки = document.querySelectorAll('.кнопка-выбора');
    всеКнопки.forEach(function(к) { к.disabled = true; });

    var результат = текущиеВыборы[индекс].эффект();

    setTimeout(function() {
        // Суточный расход ресурсов при каждом выборе
        применитьСуточныйРасход();

        var текстовоеПоле = document.getElementById('текст-диалога');
        if (текстовоеПоле && результат) {
            текстовоеПоле.dataset.полныйТекст = результат;
            текстовоеПоле.onclick = function() { пропуститьПечать(); };
            печататьТекст(текстовоеПоле, результат, 18, null);
        }

        обновитьИнтерфейс();

        // Вспышка экрана при потере здоровья
        if (игра.здоровье <= 20) {
            document.body.style.transition = 'background 0.1s';
            document.body.style.background = 'rgba(139,0,0,0.3)';
            setTimeout(function() { document.body.style.background = ''; }, 200);
        }

        // Проверка смерти в любой момент
        if (игра.еда <= 0 || игра.вода <= 0 || игра.здоровье <= 0 || игра.рассудок <= 0) {
            setTimeout(проверитьКонцовки, 1500);
            return;
        }

        игра.текущийДиалог++;
        // Автосохранение — облако если есть SDK, иначе localStorage
        if (sdkГотов && яПлеер) { сохранитьОблако(); } else { сохранитьИгру(); }

        if (игра.текущийДиалог > 30) {
            setTimeout(проверитьКонцовки, 2000);
        } else {
            setTimeout(function() { загрузитьДиалог(игра.текущийДиалог); }, 2000);
        }
    }, 500);
}

